package controlflowstatements;

/*    *
        *
          *
            *

 */

public class StarPattern4 {
    public static void main(String[] args)
    {
        int no=5;
         for(int i=1;i<no;i++) {
             System.out.println(" * ");
             for (int j = 0; j < i; j++) {
                 System.out.print("  ");
             }
         }
             for(int k=5;k>0;k--)
             {
                 System.out.println(" * ");

             for(int m=0;m<k;m++)
             {
                 System.out.print("  ");
             }
             }
         }

    }

